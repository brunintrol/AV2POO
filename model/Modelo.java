package model;

public class Modelo {
	String id;
	String nome;
	String idmarca;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getIdmarca() {
		return idmarca;
	}
	public void setIdmarca(String idmarca) {
		this.idmarca = idmarca;
	}
}
